public class UserInfo {

    String userName;
    String userPass;

    public UserInfo(){

    }
    public UserInfo(String userName, String userPass){
        userName=this.userName;
        userPass=this.userPass;
    }
    public void setUserName(String userName){
        userName=this.userName;
    }
    public void setUserPass(String userPass){
        userPass=this.userPass;
    }



}
